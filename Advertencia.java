public class Advertencia {
    private String motivo;
    private boolean suspencao;
    
    public String getMotivo() {
        return motivo;
    }
    public void setMotivo(String motivo) {
        this.motivo = motivo;
    }
    public boolean isSuspencao() {
        return suspencao;
    }
    public void setSuspencao(boolean suspencao) {
        this.suspencao = suspencao;
    }
    
}
